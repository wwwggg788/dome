function getLanguageFromUrl() {
    const path = window.location.pathname;
    const match = path.match(/^\/([a-z]{2})\//);
    return match ? match[1] : 'pt-br'; // 默认葡语
}

// 切换语言函数
function switchLanguage(lang) {
    // 更新内容
    document.querySelectorAll('[data-lang]').forEach(el => {
        const key = el.getAttribute('data-lang');
        if (translations[lang][key]) {
            if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
                el.placeholder = translations[lang][key];
            } else {
                el.textContent = translations[lang][key];
            }
        }
    });

    const langIcon = document.getElementById('lang-icon');
    langIcon.src = `assets/svg/${lang}.svg`;

    localStorage.setItem('tf-lang', lang);
    document.documentElement.lang = lang;

    // 更新URL路径
    updateUrlWithLang(lang);
}

// 更新URL路径中的语言部分
function updateUrlWithLang(lang) {
    const currentPath = window.location.pathname;
    const currentLang = getLanguageFromUrl();

    // 替换现有语言路径或添加新的语言路径
    let newPath;
    if (currentLang) {
        newPath = currentPath.replace(`/${currentLang}/`, `/${lang}/`);
    } else {
        // 如果当前URL没有语言前缀，添加语言前缀
        newPath = `/${lang}${currentPath}`;
    }

    // 保留查询参数和哈希
    const search = window.location.search;
    const hash = window.location.hash;

    // 使用pushState避免页面刷新
    window.history.pushState(null, null, newPath + search + hash);
}

// 初始化页面语言
document.addEventListener('DOMContentLoaded', () => {
    const initialLang = getLanguageFromUrl();
    switchLanguage(initialLang);

    // 绑定按钮点击事件
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.addEventListener('click', () => {
            const lang = btn.getAttribute('data-lang');
            switchLanguage(lang);
        });
    });
});